public class pattern2 {
    public static void main(String args[])
    {
        int n=5;
        for(int i=1;i<=n;i++){//outer loop upto 5
            for(int j=1;j<=i;j++){//innerloop
                System.out.print(j);// prints j value  

            }
            System.out.println();

        }
    }
    
}
