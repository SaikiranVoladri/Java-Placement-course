public class switchstatement {
    public static void main (String args[])
    {
        int n=3;
        switch(n){
            case 1:System.out.println("one");
            break;
            case 3:System.out.println("three");
            break;
            case 2:System.out.println("two");
            break;
            default:System.out.println("invalid");
        }
    }
    
}
